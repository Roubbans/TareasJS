let mes = parseInt(prompt("Ingrese el numero de su mes"));

switch(mes)
{
    case 1:
        alert(`El numero ${mes} corresponde al mes de Enero`); break;
    case 2:
        alert(`El numero ${mes} corresponde al mes de Febrero`); break;
    case 3:
        alert(`El numero ${mes} corresponde al mes de Marzo`); break;
    case 4:
        alert(`El numero ${mes} corresponde al mes de Abril`); break;
    case 5:
        alert(`El numero ${mes} corresponde al mes de Mayo`); break;
    case 6:
        alert(`El numero ${mes} corresponde al mes de Junio`); break;
    case 7:
        alert(`El numero ${mes} corresponde al mes de Julio`); break;
    case 8:
        alert(`El numero ${mes} corresponde al mes de Agosto`); break;
    case 9:
        alert(`El numero ${mes} corresponde al mes de Septiembre`); break;
    case 10:
        alert(`El numero ${mes} corresponde al mes de Octubre`); break;
    case 11:
        alert(`El numero ${mes} corresponde al mes de Noviembre`); break;
    case 12:
        alert(`El numero ${mes} corresponde al mes de Diciembre`); break;
    default: alert(`${mes} este numero no corresponde a ningun mes`);break;
   
            
}